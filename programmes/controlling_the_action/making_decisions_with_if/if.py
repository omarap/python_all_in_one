"""
        SYNTAX
if condition: do this
do this no matter what

"""
#Example
#Result of a simple if when the condition proves true
sun = "down"
if sun == "down" : print("Goodnight!")
print("I am here")

#Result of if when condition proves false
sun = "up"
if sun == "down" : print("Goodnight")
print("I am here ")

