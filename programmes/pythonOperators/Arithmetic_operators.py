#Arithmetic operators
"""
Arithmetic operators as the name implies are for doing arithmetics; 
addition, subtraction, division, multiplication, modulus etc..

"""

#Python Arithmetic operators
#Addition (+)
print (1 + 1)
#Subtraction(-)
print (10 - 9)
#Multiplication(*)
print (3 * 5)
#Division(/)
print (10 / 5 )
#Modulus ( % remainder after division)
print (11 % 5 )
#Exponent(** raised to the power of.)
print (3**2)
#Floor division(//)
print (11 // 5)
